package lectures.part1

object ValuesVariablesTypes extends App {

  val a: Int = 42
  println(a)

  //type inference
  val b = 24
  println(b)

  val someString = "Hello, this is a string"
  print(someString)

  //primitives from java
  val bool : Boolean = false

  val character : Char = 'a'

  val byte : Byte = 1

  val short : Short = 123

  val int : Int = 42

  val long : Long = 100L

  val double : Double = 3.14

  val float : Float = 2.0f

}